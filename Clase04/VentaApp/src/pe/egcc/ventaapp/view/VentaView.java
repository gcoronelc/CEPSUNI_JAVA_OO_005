  private void mostrar(Item[] repo) {
    // Acceder al modelo
    DefaultTableModel tabla;
    tabla = (DefaultTableModel) tblRepo.getModel();
    // Limpiar tabla
    tabla.setRowCount(0);
    // LLenar tabla
    for (Item item : repo) {
      Object[] rowData = {item.getConcepto(), item.getValor()};
      tabla.addRow(rowData);
    }
  }
