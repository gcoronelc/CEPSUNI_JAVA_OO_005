  private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
    try {
      // Verificar combo
      if(cboTipo.getSelectedIndex() == -1){
       throw new RuntimeException("Falta seleccionar tipo de comprobante.");
      }
      // Datos
      TipoComp tipo = (TipoComp) cboTipo.getSelectedItem();
      double total = Double.parseDouble(txtTotal.getText());
      // Proceso
      VentaController control = new VentaController();
      Item[] repo = control.procesar(tipo, total);
      // Mostrar reporte
      mostrar(repo);
    } catch (NumberFormatException e) {
      JOptionPane.showMessageDialog(rootPane, "El total es un n√∫mero positivo.",
              "VENTA ERROR",JOptionPane.ERROR_MESSAGE);
    }  catch (RuntimeException e) {
      JOptionPane.showMessageDialog(rootPane, e.getMessage(),
              "VENTA ERROR",JOptionPane.ERROR_MESSAGE);
    }
    
  }//GEN-LAST:event_btnProcesarActionPerformed

