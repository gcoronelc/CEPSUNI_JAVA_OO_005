public class PrestamoModel {
    
    //Entrada
    private int meses;
    private double capital;
    private double intMes;
    private double cuotaAPagar;
    
    public int getMeses() {
        return meses;
    }

    public void setMeses(int meses) {
        this.meses = meses;
    }

    public double getCapital() {
        return capital;
    }

    public void setCapital(double capital) {
        this.capital = capital;
    }

    public double getIntMes() {
        return intMes;
    }

    public void setIntMes(double intMes) {
        this.intMes = intMes;
    }

    public double getCuotaAPagar() {
        return cuotaAPagar;
    }

    public void setCuotaAPagar(double cuotaAPagar) {
        this.cuotaAPagar = cuotaAPagar;
    }
    

        
}
